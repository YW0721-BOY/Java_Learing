package com.HM.arithmeticoperator;

public class ArithmeticoperatorDemo1  {
    public static void main(String[] args) {
        System.out.println("加法："+3+2);
        System.out.println("减法："+ (3-2));
        System.out.println("乘法"+3*2);

        //小数计算不精确
        System.out.println(1.1+1.01);

        System.out.println();
        System.out.println("取模/取余："+5%2);
        System.out.println("除数："+5/2);
    }
}
